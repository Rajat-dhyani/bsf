    private void update()
    {
         try{
           String sql =  "Select * from FamilyInformation where RegimentalNo=?";
           ps = con.prepareStatement(sql);
           ps.setInt(1, Integer.parseInt(jTRegimentalNo.getText()));
           rs = ps.executeQuery();
           
           if(rs.next())
           {
            this.jTNameOfCourse.setText(rs.getString(2));
            this.jTNameoftheinstitute.setText(rs.getString(3));
            this.jTDegree.setText(rs.getString(4));
            this.jTYear.setText(rs.getString(5));
            this.jTGrade.setText(rs.getString(6));
            this.jTNameOfCourse1.setText(rs.getString(7));
            this.jTNameoftheinstitute1.setText(rs.getString(8));
            this.jTDegree1.setText(rs.getString(9));
            this.jTYear1.setText(rs.getString(10));
            this.jTGrade1.setText(rs.getString(11));
            this.jTNameoftheinstitute2.setText(rs.getString(8));
            this.jTDegree2.setText(rs.getString(9));
            this.jTYear2.setText(rs.getString(10));
            this.jTGrade2.setText(rs.getString(11));
            }
           else
           {
               JOptionPane.showMessageDialog(null, "Record Not Found");
           }
           ps.close();
           rs.close();
         }
           catch(Exception ex)
           {
                   
           }
        
    }
    
