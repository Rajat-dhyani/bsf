    private void jBSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSubmitActionPerformed
        // TODO add your handling code here:
         try{
            String st = "Insert into CourseDetails (RegimentalNo,Name_of_the_Primary_Course,Primary_Institute,Primary_Degree,Primary_Year_Of_Completion,Primary_Grade,Name_of_the_Secondary_Course,Secondary_Institute,Secondary_Degree,Secondary_Year_Of_Completion,Secondary_Grade,Name_of_Higher_course,Higher_Institute,Higher_Degree,Higher_Year_Of_Completion,Higher_Grade) values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            
            ps = con.prepareStatement(st);
            
            ps.setInt(1, Integer.parseInt(jTRegimentalNo.getText()));
            ps.setString(2, jTNameOfCourse.getText());
            ps.setString(3, jTNameoftheinstitute.getText());
            ps.setString(4, jTDegree.getText());
            ps.setString(5, jTYear.getText());
            ps.setString(6, jTGrade.getText());
            
            ps.setString(7, jTNameOfCourse1.getText());
            ps.setString(8, jTNameoftheinstitute1.getText());
            ps.setString(9, jTDegree1.getText());
            ps.setString(10, jTYear1.getText());
            ps.setString(11, jTGrade1.getText());
            
            ps.setString(12, jTNameOfCourse2.getText());
            ps.setString(13, jTNameoftheinstitute2.getText());
            ps.setString(14, jTDegree2.getText());
            ps.setString(15, jTYear2.getText());
            ps.setString(16, jTGrade2.getText());
            
            rs= ps.executeQuery();

            ps.close();
            rs.close();
            
            if (jOperation.getText().equals("update"))
                    JOptionPane.showMessageDialog(rootPane, "record Successfully Updated ");
            else    
                    JOptionPane.showMessageDialog(rootPane, "record Successfully Added ");
        
            new info().setVisible(true);
         this.setVisible(false);
         }
         catch(Exception ex)
         {
             
         }
    }//GEN-LAST:event_jBSubmitActionPerformed

