    private void jMExportToPdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMExportToPdfActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new java.io.File("."));
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setDialogTitle("Save");
        if ( fileChooser.showOpenDialog(jMExportToPdf) == JFileChooser.APPROVE_OPTION)
        {
        }
         String str = fileChooser.getSelectedFile().getAbsolutePath();
         str = str.replace("\\", "\\\\");
         DBMS.pdfConvertor(personalInfo,str+"\\BSF.pdf");
         JOptionPane.showMessageDialog(null,"File Saved");
    }//GEN-LAST:event_jMExportToPdfActionPerformed

