    private void jMDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMDeleteActionPerformed
        // TODO add your handling code here:
        int reg = Integer.parseInt(JOptionPane.showInputDialog("Enter Regimental No :"));
        try{  
             if(JOptionPane.showConfirmDialog(null,"Are you sure ?","Delete",JOptionPane.YES_NO_OPTION )==0)
            {
                DBMS.deleteRecord(reg, "PersonalInformation");
                DBMS.deleteRecord(reg, "Running");
                DBMS.deleteRecord(reg, "LeaveDetails");
                DBMS.deleteRecord(reg, "FamilyInformation");
                DBMS.deleteRecord(reg, "CourseDetails");
                DBMS.updateTable(personalInfo, "PersonalInformation");
                DBMS.updateTable(runningDetails, "Running");
                DBMS.updateTable(leaveDetails, "LeaveDetails");
                DBMS.updateTable(familyInfo, "FamilyInformation");
                DBMS.updateTable(courseDetails, "CourseDetails");
                
            }
        }
        catch(Exception exception)
        {
            JOptionPane.showMessageDialog(null, "Please Enter Numeric Digits");
        }
        
    }//GEN-LAST:event_jMDeleteActionPerformed

